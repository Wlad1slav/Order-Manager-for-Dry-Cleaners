public class Utils {
    public static int greaterOrEqualZero(int num) {
        // Перевіряє, щоб число не було менше нуля. Повертає int.
        if (num >= 0) return num;
        else return 0;
    }
    public static float greaterOrEqualZero(float num) {
        // Перевіряє, щоб число не було менше нуля. Повертає float.
        if (num >= 0) return num;
        else return 0;
    }
}